    private void bt_escanearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_escanearActionPerformed
        // TODO add your handling code here:
        //ip_inicio = "192.168.100.1";
        //ip_final = "192.168.100.20";
        tx_estado.setText("Escaneando... ");
        
        ip_inicio = tx_ip_inicio.getText();
        ip_final = tx_ip_final.getText();

        ips_inicio = ip_inicio.split("\\.");
        ips_final = ip_final.split("\\.");
        
        ipi = Integer.parseInt(ips_inicio[3]);
        ipf = Integer.parseInt(ips_final[3]);
        
        int p = 0;
        direcciones = new String[(ipf-ipi)+1];
        
        for (int i = ipi; i <= ipf; i++) {
            try {
                String ip = ips_inicio[0]+"."+ips_inicio[1]+"."+ips_inicio[2]+"."+i;   
                
                InetAddress inet = InetAddress.getByName(ip);
                System.err.print(ip+" ");
                if(inet.isReachable(5000)){
                    System.out.println("Host is reachable");
                    direcciones[p] = ip;
                    DefaultMutableTreeNode nodo = new DefaultMutableTreeNode(ip);
                    modelo.insertNodeInto(nodo,nodoRed,p);
                    p++;
                
                } else {
                    System.out.println("Host is NOT reachable");
                }
            } catch (Exception ex) {
                Logger.getLogger(IAnalizador.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(rootPane, ex.toString(),"ERROR",0);
            }
        }
        tx_estado.setText("Escaneo de red completo");
        
        
    }//GEN-LAST:event_bt_escanearActionPerformed

    private void bt_escaner_puertosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_escaner_puertosActionPerformed
        // TODO add your handling code here:
        int ipuerto = Integer.parseInt(tx_ipuerto.getText());
        int fpuerto = Integer.parseInt(tx_fpuerto.getText());
        String ip_puertos = tx_ip_escaner.getText();
        
        DefaultMutableTreeNode nodoScaner = new DefaultMutableTreeNode(ip_puertos);
        DefaultTreeModel modeloEscaner = new DefaultTreeModel(nodoScaner);
        TreePuertos.setModel(modeloEscaner);
        
        
        Socket sc;
        for(int i=ipuerto; i<=fpuerto; i++){
            try{
                sc = new Socket(ip_puertos,i);
                System.out.println("Puerto "+i+" Abierto");
                DefaultMutableTreeNode nodo = new DefaultMutableTreeNode(i);
                modelo.insertNodeInto(nodo,nodoScaner,0);
                sc.close();
            }
            catch(Exception ex){}
        }
        
    }//GEN-LAST:event_bt_escaner_puertosActionPerformed

